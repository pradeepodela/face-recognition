import cv2
import face_recognition
import numpy as np


####LODEING THE IMAGES AND CHANGEING THEIR COLOR ###
imgElon = face_recognition.load_image_file('selenatest.jpg')
imgElon = cv2.cvtColor(imgElon, cv2.COLOR_BGR2RGB)
imgTest = face_recognition.load_image_file('selena.jpg')
imgTest = cv2.cvtColor(imgTest, cv2.COLOR_BGR2RGB)


#### GETTING FACE LOCATIONS OF TRAIN IMAGE AND DRAWING A RECTANGLE ###
faceLoc = face_recognition.face_locations(imgElon)[0]
encodeElon = face_recognition.face_encodings(imgElon)[0]
cv2.rectangle(imgElon, (faceLoc[3], faceLoc[0]), (faceLoc[1], faceLoc[2]), (255, 0, 255), 2)


#### GETTING FACE LOCATIONS OF TEST IMAGE AND DRAWING A RECTANGLE ###
faceLocTest = face_recognition.face_locations(imgTest)[0]
encodeTest = face_recognition.face_encodings(imgTest)[0]
cv2.rectangle(imgTest, (faceLocTest[3], faceLocTest[0]), (faceLocTest[1], faceLocTest[2]), (255, 0, 255), 2)


#### COMPARING THE IMAGES AND PUTTING THE RESULT######
results = face_recognition.compare_faces([encodeElon], encodeTest)
faceDis = face_recognition.face_distance([encodeElon], encodeTest)
print(results, faceDis)
cv2.putText(imgTest, f'{results} {round(faceDis[0], 2)}', (50, 50), cv2.FONT_HERSHEY_COMPLEX, 1, (0, 0, 255), 2)



